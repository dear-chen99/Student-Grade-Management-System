"""
学生成绩管理系统 - 源代码包.

包含主应用、UI组件和工具函数。

本包为项目的源代码顶层包，负责在导入时自动将项目根目录添加到
``sys.path`` 中，确保无论以何种方式启动（直接运行模块或通过主入口）
都能正确解析项目内各模块的绝对导入。

Attributes:
    BASE_DIR: 项目根目录的绝对路径。
"""

import os
import sys

# ------------------------------------------------------------------
# 项目路径配置
# ------------------------------------------------------------------
# 将项目根目录添加到 Python 路径，保证包内模块能够正确导入同级及上级模块。
BASE_DIR = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
if BASE_DIR not in sys.path:
    sys.path.insert(0, BASE_DIR)
